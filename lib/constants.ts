export const normalizeSymbol = (symbol: string): string => {
  if (!symbol) return "";
  const decoded = decodeURIComponent(String(symbol));
  const cleaned = decoded
    .replace(/^(BSE|NSE)(:|%3A)?/i, "")
    .replace(/[^A-Z0-9.\-]/gi, "");
  return `BSE:${cleaned.toUpperCase()}`;
};

